package com.jobesk.boothbook.fragments.calanderfrag.modelsbooking;

public class Venue {

    private String venue_name;
    private String venue_address;
    private String venue_postcode;
    private String business_country;

    public String getVenue_name() {
        return venue_name;
    }

    public void setVenue_name(String venue_name) {
        this.venue_name = venue_name;
    }

    public String getVenue_address() {
        return venue_address;
    }

    public void setVenue_address(String venue_address) {
        this.venue_address = venue_address;
    }

    public String getVenue_postcode() {
        return venue_postcode;
    }

    public void setVenue_postcode(String venue_postcode) {
        this.venue_postcode = venue_postcode;
    }

    public String getBusiness_country() {
        return business_country;
    }

    public void setBusiness_country(String business_country) {
        this.business_country = business_country;
    }
}
